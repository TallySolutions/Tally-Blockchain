#!/bin/bash

function createTally() {
  infoln "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/tally.tallysolutions.com/

  export FABRIC_CA_CLIENT_HOME=${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname tally --tls.certfiles "${HOME}/fabric/tally-network/organizations/fabric-ca/tally/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-tally.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-tally.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-tally.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-tally.pem
    OrganizationalUnitIdentifier: orderer' > "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy tally's CA cert to tally's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/msp/tlscacerts"
  cp "${HOME}/fabric/tally-network/organizations/fabric-ca/tally/ca-cert.pem" "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/msp/tlscacerts/ca.crt"

  # Copy tally's CA cert to tally's /tlsca directory (for use by clients)
  mkdir -p "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/tlsca"
  cp "${HOME}/fabric/tally-network/organizations/fabric-ca/tally/ca-cert.pem" "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/tlsca/tlsca.tally.tallysolutions.com-cert.pem"

  # Copy tally's CA cert to tally's /ca directory (for use by clients)
  mkdir -p "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/ca"
  cp "${HOME}/fabric/tally-network/organizations/fabric-ca/tally/ca-cert.pem" "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/ca/ca.tally.tallysolutions.com-cert.pem"

  infoln "Registering tbchlfdevpeer01 "
  set -x
  fabric-ca-client register --caname tally --id.name tbchlfdevpeer01 --id.secret tbchlfdevpeer01pw --id.type peer --tls.certfiles "${HOME}/fabric/tally-network/organizations/fabric-ca/tally/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering user"
  set -x
  fabric-ca-client register --caname tally --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${HOME}/fabric/tally-network/organizations/fabric-ca/tally/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering the org admin"
  set -x
  fabric-ca-client register --caname tally --id.name tallyadmin --id.secret tallyadminpw --id.type admin --tls.certfiles "${HOME}/fabric/tally-network/organizations/fabric-ca/tally/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Generating the tbchlfdevpeer01 msp"
  set -x
  fabric-ca-client enroll -u https://tbchlfdevpeer01:tbchlfdevpeer01pw@localhost:7054 --caname tally -M "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/peers/tbchlfdevpeer01.tally.tallysolutions.commsp" --tls.certfiles "${HOME}/fabric/tally-network/organizations/fabric-ca/tally/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/msp/config.yaml" "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/peers/tbchlfdevpeer01.tally.tallysolutions.commsp/config.yaml"

  infoln "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://tbchlfdevpeer01:tbchlfdevpeer01pw@localhost:7054 --caname tally -M "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/peers/tbchlfdevpeer01.tally.tallysolutions.comtls" --enrollment.profile tls --csr.hosts tbchlfdevpeer01 --csr.hosts localhost --tls.certfiles "${HOME}/fabric/tally-network/organizations/fabric-ca/tally/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/peers/tbchlfdevpeer01.tally.tallysolutions.comtls/tlscacerts/"* "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/peers/tbchlfdevpeer01.tally.tallysolutions.comtls/ca.crt"
  cp "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/peers/tbchlfdevpeer01.tally.tallysolutions.comtls/signcerts/"* "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/peers/tbchlfdevpeer01.tally.tallysolutions.comtls/server.crt"
  cp "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/peers/tbchlfdevpeer01.tally.tallysolutions.comtls/keystore/"* "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/peers/tbchlfdevpeer01.tally.tallysolutions.comtls/server.key"

  infoln "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname tally -M "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/users/User1@tally.tallysolutions.com/msp" --tls.certfiles "${HOME}/fabric/tally-network/organizations/fabric-ca/tally/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/msp/config.yaml" "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/users/User1@tally.tallysolutions.com/msp/config.yaml"

  infoln "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://tallyadmin:tallyadminpw@localhost:7054 --caname tally -M "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/users/Admin@tally.tallysolutions.com/msp" --tls.certfiles "${HOME}/fabric/tally-network/organizations/fabric-ca/tally/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/msp/config.yaml" "${HOME}/fabric/tally-network/organizations/peerOrganizations/tally.tallysolutions.com/users/Admin@tally.tallysolutions.com/msp/config.yaml"
}


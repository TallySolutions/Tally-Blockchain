  # Copy orderer org's CA cert to orderer's /msp/tlscacerts directory (for use in the orderer MSP definition)
  mkdir -p "${HOME}/fabric/tally-network/organizations/ordererOrganizations/tally.tallysolutions.com/orderers/orderer.tallysolutions.com/msp/tlscacerts"
  cp "${HOME}/fabric/tally-network/organizations/ordererOrganizations/tally.tallysolutions.com/orderers/orderer.tallysolutions.com/tls/tlscacerts/"* "${HOME}/fabric/tally-network/organizations/ordererOrganizations/tally.tallysolutions.com/orderers/orderer.tallysolutions.com/msp/tlscacerts/tlsca.tally.tallysolutions.com-cert.pem"

  infoln "Generating the admin msp"
  set -x
  fabric-ca-client enroll -u https://ordererAdmin:ordererAdminpw@localhost:9054 --caname ca-orderer -M "${HOME}/fabric/tally-network/organizations/ordererOrganizations/tally.tallysolutions.com/users/Admin@tallysolutions.com/msp" --tls.certfiles "${HOME}/fabric/tally-network/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${HOME}/fabric/tally-network/organizations/ordererOrganizations/tally.tallysolutions.com/msp/config.yaml" "${HOME}/fabric/tally-network/organizations/ordererOrganizations/tally.tallysolutions.com/users/Admin@tallysolutions.com/msp/config.yaml"
